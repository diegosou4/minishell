#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
	lst = NULL;
}

// Del e uma funcao que faz o free dentro do content
// chamamos a funcao ela faz o free dentro do content
